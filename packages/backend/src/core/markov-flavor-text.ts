// NGワード
const NG_WORDS = ['死', '殺', '暴力', '虐待'];

/**
 * 配列からランダムに1つ選択
 */
function randomPick<T>(array: T[]): T {
	return array[Math.floor(Math.random() * array.length)];
}

/**
 * NGワードを含むかチェック
 * @param text チェック対象のテキスト
 * @returns NGワードを含む場合true
 */
export function containsNgWord(text: string): boolean {
	return NG_WORDS.some(word => text.includes(word));
}

/**
 * マルコフ連鎖フレーバーテキスト生成
 * @param type ペットの種類（'cow' | 'chicken'）
 * @returns 生成されたフレーバーテキスト（最大256文字）
 */
export function generateFlavorText(type: 'cow' | 'chicken'): string {
	const MAX_ATTEMPTS = 10;

	for (let i = 0; i < MAX_ATTEMPTS; i++) {
		// 1. 各カテゴリからランダムに単語を選択
		const personality = randomPick(PERSONALITY_WORDS);
		const action = type === 'cow'
			? randomPick(COW_ACTIONS)
			: randomPick(CHICKEN_ACTIONS);
		const modifier = randomPick(MODIFIERS);

		// 2. パターンを組み立て（2パターン）
		// パターン1: 修飾語 + 性格 + 動物 + 助詞 + 行動
		// パターン2: 性格 + 動物 + 助詞 + 修飾語 + 行動
		const pattern = Math.random() < 0.5 ? 1 : 2;
		const animal = type === 'cow' ? '牛' : '鶏';
		const particle = Math.random() < 0.5 ? 'が' : 'は';

		let text: string;
		if (pattern === 1) {
			text = `${modifier}${personality}${animal}${particle}${action}`;
		} else {
			text = `${personality}${animal}${particle}${modifier}${action}`;
		}

		// 3. NGワードチェック
		if (!containsNgWord(text)) {
			return text;
		}
	}

	// フォールバック（NGワードチェックが全て失敗した場合）
	const animal = type === 'cow' ? '牛' : '鶏';
	return `のんびりした${animal}がいる`;
}
